# Task (b): Sentence to Wordlist and Reverse

# Step 1: Define the sentence
sentence = "Data analysis is essential for business decisions"

# Step 2: Convert to word list
wordList = sentence.split()

# Step 3: Reverse the word list
reversedWordList = list(reversed(wordList))

# Output Results
print("Original Sentence:", sentence)
print("Word List:", wordList)
print("Reversed Word List:", reversedWordList)
